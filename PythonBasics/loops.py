# Цикл по диапазону чисел
for i in range(5):           # 0, 1, 2, 3, 4
    print(f"Шаг: {i}")

for i in range(1, 6):        # 1, 2, 3, 4, 5
    print(f"Число: {i}")

for i in range(1, 10, 2):    # 1, 3, 5, 7, 9 (шаг 2)
    print(f"Нечетное: {i}")

# Цикл по элементам списка
фрукты = ["яблоко", "банан", "апельсин"]
for фрукт in фрукты:
    print(f"Фрукт: {фрукт}")

# Цикл по строке
for буква in "Python":
    print(буква)


# Простой цикл while
счетчик = 0
while счетчик < 5:
    print(f"Счетчик: {счетчик}")
    счетчик += 1  # увеличиваем счетчик на 1

# Бесконечный цикл с break
while True:
    команда = input("Введите 'стоп' для выхода: ")
    if команда == "стоп":
        break  # выход из цикла
    print(f"Вы ввели: {команда}")

