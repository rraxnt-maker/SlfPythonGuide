# Арифметические операции
a = 10 + 5    # 15
b = 10 - 5    # 5  
c = 10 * 5    # 50
d = 10 / 3    # 3.333...
e = 10 // 3   # 3 (целочисленное деление)
f = 10 % 3    # 1 (остаток от деления)
g = 2 ** 3    # 8 (возведение в степень)

# Сравнение
x = 5 > 3     # True
y = 5 == 3    # False
z = 5 != 3    # True

